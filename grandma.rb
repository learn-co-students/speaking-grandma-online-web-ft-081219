  end
  
  if 
    phrase == 'Hi Nana, how are you?'
    says = "HUH?! SPEAK UP, SONNY!"
    return says
  end
  
  if 
    phrase == "Hi!"
    says = "HUH?! SPEAK UP, SONNY!"
    return says
  end
  
  if 
    phrase == "WHAT DID YOU EAT TODAY?"
    says = "NO, NOT SINCE 1938!"
    return says
  end 
  
  if 
    phrase == 'WHAT?'
    says = "NO, NOT SINCE 1938!"
    return says
  end 

end 

 
  
  
